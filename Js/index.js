const mainNav = document.querySelector('.main-nav');
const navToggle = document.querySelector('.nav-toggle');

function closeMobileNav() {
  mainNav?.classList.remove('is-open');
  if (navToggle) {
    navToggle.setAttribute('aria-expanded', 'false');
    navToggle.classList.remove('is-open');
  }
}

navToggle?.addEventListener('click', function (e) {
  // 1. Зупиняємо будь-яку стандартну дію браузера
  e.preventDefault();
  e.stopPropagation();

  if (!mainNav) return;

  // 2. Визначаємо, що ми робимо: відкриваємо чи закриваємо
  const isOpening = !mainNav.classList.contains('is-open');

  if (isOpening) {
    // Скролимо вгору ТІЛЬКИ при відкритті
    window.scrollTo({
      top: 0,
      behavior: 'smooth'
    });
  } else {

    const currentScroll = window.scrollY;
    setTimeout(() => {
        window.scrollTo(0, currentScroll);
    }, 10);
  }

  // 3. Звичайна логіка перемикання
  const open = mainNav.classList.toggle('is-open');
  navToggle.setAttribute('aria-expanded', open ? 'true' : 'false');
  navToggle.classList.toggle('is-open', open);
});



